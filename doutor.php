                            <b>Instituto do Câncer Arnaldo Vieira de Carvalho (IAVC)</b><br><br>
                            

                            No IAVC realiza semanalmente cirurgias oncológicas por via laparoscópica, principalmente para câncer de próstata e de rim, além disso atua como assistente do programa de fellowship da instituição.
                            <br><br>

                            <b>Fellowship de cirurgia robótica e laser (HoLEP) no Hosptial San Raffaele – Itália</b><br><br>

                            No ano de 2018 realizou clinical fellowship em cirurgias robóticas, laparoscopia e HoLEP na Ospedalle San Raffaele em Milão na Itália sob a orientação dos professores Francesco Montorsi e Alberto Briganti. A Ospedalle San Raffaele é referencia internacional e cirurgias robóticas e pesquisa, nesse hospital o Dr. Alexandre Iscaife ampliou sua experiência em cirurgias robóticas para câncer de próstata, rim e bexiga além do HoLEP. 

