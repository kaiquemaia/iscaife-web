                            No terceiro e quarto anos da faculdade, foi aprovado em concurso para realizar estágio extracurricular na Maternidade Vila Nova Cachoeirinha, onde participava da rotina intensa dos cuidados obstétricos a mulheres em trabalho de parto. No quinto e sexto anos, realizou estágios extracurriculares na Liga de Cirurgia da UNICAMP e na Unidade de terapia Intensiva do Hospital Sírio Libanês.
