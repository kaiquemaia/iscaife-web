                            <br><br>
                            <b>Residência Médica</b><br><br>

                            Decidido a retornar a São Paulo, sua terra natal, Dr Alexandre Iscaife ingressou para a Residência Médica em cirurgia geral da Escola Paulista de Medicina/UNIFESP, onde consolidou a formação cirúrgica iniciada na graduação e confirmou dentre todas as áreas cirúrgicas, sua paixão pela Urologia.

                            Após o término dos dois anos de cirurgia geral, ingressou para a Residência Médica em Urologia da Escola Paulista de Medicina. Por três anos mostrou sua dedicação, capacidade e habilidade cirúrgica, recebendo em 2010 o premio Residente Destaque concedido pela Disciplina de Urologia da Escola Paulista de Medicina. 
                            <br><br>
                            <b>Pós-Graduação</b><br><br>

                            Ao término da Residência Médica de Urologia, após aprovação na prova de Título de Especialista em Urologia da Sociedade Brasileira de Urologia, dando prosseguimento aos interesses desenvolvidos muito precocemente durante a graduação nas áreas de biologia molecular e próstata, Dr Alexandre Iscaife ingressou no programa de doutorado da Divisão de Urologia da Faculdade de Medicina da Universidade de São Paulo (FMUSP) com bolsa de Doutorado concedida pela Fapesp (Fundação de Amparo a Pesquisa de são Paulo). Sob orientação da renomada uropatologista, Profa. Dra Katia Ramos Moreira Leite, chefe do Laboratório de investigações médicas da Urologia (LIM-55), desenvolveu a tese “O uso de MicroRNA para o tratamento do câncer de próstata: estudos in vitro e in vivo”, defendida em brilhante arguição no ano de 2016, com posterior publicação na revista Apoptosis.
                            <br><br>
                            <b>Carreira no Hospital das Clínicas da FMUSP</b><br><br>

                            Enquanto desenvolvia seu projeto de Doutorado, Dr Alexandre Iscaife foi contratado como assistente da clinica urológica no setor de próstata realizando cirurgias semanais com os residentes da urologia, as cirurgias realizadas com os residentes em treinamento são: RTU de próstata, Prostatectomia Transvesical Laparoscopica, Millin e HoLEP.
